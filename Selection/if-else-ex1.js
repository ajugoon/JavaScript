/* This is a simple program that introduces how to enable programs to make 
choices based on conditional statements. */

const size = 10;

if (size > 100) {
  console.log('Big');
} 
else if (size > 20) {
  console.log('Medium');
} 
else if (size > 4) {
  console.log('Small');
} 
else {
  console.log('Tiny');
}
